import { findNumbers, gearRatio, machineNumberSum } from './machine-numbers';

describe('findNumbers', () => {
  it('should list numbers adjacent to symbols', () => {
    expect(findNumbers(`467..114..
...*......
..35..633.
......#...
617*......
.....+.58.
..592.....
......755.
...$.*....
.664.598..`)).toEqual([467, 35, 633, 617, 592, 755, 664, 598]);
  });
});

describe('machineNumberSum', () => {
  it('should sum up all the valid machine part numbers', () => {
    expect(machineNumberSum(`467..114..
...*......
..35..633.
......#...
617*......
.....+.58.
..592.....
......755.
...$.*....
.664.598..`)).toEqual(4361);
  });
});

describe('gearRatio', () => {
  it('calculates the correct gear ratio', () => {
    expect(gearRatio(`467..114..
...*......
..35..633.
......#...
617*......
.....+.58.
..592.....
......755.
...$.*....
.664.598..`)).toEqual(467835);
  });
});
